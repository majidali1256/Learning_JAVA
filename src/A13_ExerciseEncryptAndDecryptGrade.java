import java.util.Scanner;
public class A13_ExerciseEncryptAndDecryptGrade {
    public static void main(String[] args) {
        System.out.println(" Increment and Decrement the Character ");
    //Incrementing Grade
        char a = 'B'+3;
        System.out.println(" Answer is: " + a );
    //Decrementing Grade
        char b = 'E'-3;
        System.out.println(" Answer is: " + b );
    // other way
        char c = 'C';
        c = (char) ('C'+3);
        System.out.println(" Answer is: "+ c);



    }
}
